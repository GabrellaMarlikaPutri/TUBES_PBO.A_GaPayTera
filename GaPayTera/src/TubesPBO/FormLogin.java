    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    koneksi.getKoneksi();
    
    try{
        Statement stat = (Statement) koneksi.getKoneksi().createStatement();
        String sql = "SELECT * FROM LoginKaryawan WHERE ID_Karyawan='" + id_karyawan.getText() + "' AND Password_Karyawan = '" + pass_karyawan.getText() + "'";
        ResultSet RaUser = stat.executeQuery(sql);
            
        if(RaUser.next()){
            JOptionPane.showMessageDialog(null, "Login Berhasil..", "Login Karyawan", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new Menu().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Maaf, ID atau Password salah!", "Login Karyawan", JOptionPane.INFORMATION_MESSAGE);
            id_karyawan.setText("");
            pass_karyawan.setText("");
            id_karyawan.requestFocus();
            pass_karyawan.requestFocus();
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

